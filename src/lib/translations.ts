/**
 * Intelligent helper function to translate a text string dynamically.
 * First checks for an exact match in the translation dictionary for the active language.
 * If not found, returns the English text.
 */
export function translate(text: string, lang?: LanguageCode | string): string {
  if (!lang) return text;
  const normalizedLang = lang.toLowerCase() as LanguageCode;
  
  if (!TRANSLATIONS[normalizedLang]) {
    return text;
  }
  
  const dict = TRANSLATIONS[normalizedLang];
  
  // Exact match
  if (dict[text]) {
    return dict[text];
  }

  // Fallback to English dict exact match
  const englishDict = TRANSLATIONS.en;
  const matchKey = Object.keys(englishDict).find(key => key.toLowerCase() === text.toLowerCase());
  if (matchKey && dict[matchKey]) {
    return dict[matchKey];
  }

  return text;
}
